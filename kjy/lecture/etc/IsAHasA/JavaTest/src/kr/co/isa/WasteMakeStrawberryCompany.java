package kr.co.isa;

public class WasteMakeStrawberryCompany {

	String strawberry;
	String milk;
	String strawberryPowder;
	String milkPowder;
	
	public WasteMakeStrawberryCompany() {
		this.strawberry = "저품질 딸기";
		this.milk = "저품질 딸기";
		this.strawberryPowder = "저품질 딸기분말";
		this.milkPowder = "저품질 우유분말";
	}
	
	
	public String getStrawberryPowder() {
		return strawberryPowder;
	}
	
	public String getMilkPowder() {
		return milkPowder;
	}
}
